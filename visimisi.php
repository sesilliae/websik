                    <p><b>Visi Keilmuan</b>
                        <br><br>
                        Mengembangkan dan mengimplementasikan sistem informasi untuk mendukung perkembangan teknologi pada bidang kelautan dan menghasilkan lulusan yang berdayasaing serta ahli dalam pengolahan data numerik maupun spatial.
                        <br><br>

                        <b>Misi</b>
                        <br><br>
                        1. Menyelenggarakan layanan pendidikan dan pengajaran berstandar nasional bidang keahlian sistem informasi kelautan,<br>
                        2. Melaksanakan penelitian di bidang sistem informasi kelautan dan inovasinya dalam upaya mengembangkan potensi sumberdaya hayati pesisir dan laut di daerah dan nasional,<br>
                        3. Menerapkan teknologi informasi kelautan yang terpadu, produktif dan terukur dalam pendidikan, penelitian, dan pengabdian serta pemberdayaan kepada masyarakat berbasis hasil penelitian.<br>
                        4. Mengembangkan dan menyebarluaskan keilmuan dan teknologi bidang kelautan yang berbasis pada kebutuhan masyarakat dan industri.<br>
